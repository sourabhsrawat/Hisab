package com.rawat.hisab.DB;

public class CardSetting {
	
	private int id;
	private int cardInv;
	private int cardTotal;
	
	public int getID()
	{
		return id;
	}
	
	public void setID(int id)
	{
		this.id=id;
	}
	
	public int getCardInv()
	{
		return cardInv;
	}
	
	public void setCardInv(int cardInv)
	{
		this.cardInv=cardInv;
	}
	
	public int getCardTotal()
	{
		return cardTotal;
	}
	
	public void setCardTotal(int cardTotal)
	{
		this.cardTotal=cardTotal;
	}

}
