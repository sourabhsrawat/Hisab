package com.rawat.hisab.DB;

public class CardDetails {
	
	private int id;
	private String name;
	private int amt;
	private String mnt;
	private int yr;
	
	public int getID()
	{
		return id;
	}
	
	public void setID(int id)
	{
		this.id=id;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name=name;
	}
	
	public int getAmt()
	{
		return amt;
	}
	
	public void setAmt(int amt)
	{
		this.amt=amt;
	}
	
	public String getMnt()
	{
		return mnt;
	}
	
	public void setMnt(String mnt)
	{
		this.mnt=mnt;
	}
	
	public int getYr()
	{
		return yr;
	}
	
	public void setYr(int yr)
	{
		this.yr=yr;
	}

	
}
